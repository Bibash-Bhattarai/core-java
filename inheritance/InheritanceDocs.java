package inheritance;

public class InheritanceDocs {

	/*
	 * ---------------Inheritance-------
	 * 
	 * # use for code-reusability
	 * # process of accessing properties and methods of parent/super/base class to child/sub/derived class is known as inheritance
	 * 
	 * # Also known as IS-A relationship
	 * 
	 * # Also called Generalization to specialization
	 * 
	 * # we can inherit instance and static variables and methods.
	 * # we can't inherit private properties , method and constructors.
	 * # we can inherit only one class at a time.
	 * 		(i.e multiple inheritance not supported in java with class)
	 * 
	 *  	# syntax:
	 *  
	 *  		child-class extends parent-class{
	 *  
	 *  				// properties : parent + child
	 *  
	 *  				// methods : parent + child
	 *  
	 *  		}
	 */
}
