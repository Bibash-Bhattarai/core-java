package constructor;

public class ConstructorDocs {
	
	/*
	 * ------------Constructor-------
	 * # used to create object in java
	 * # special type of method which has same name as class name.
	 * # no return type 
	 * # if there is no any user defined constructor in class then java provides default constructor to create object
	 * 
	 *  
	 *  # Two types of constructor :
	 *  
	 *  		a. Default Constructor
	 *  			# used to create object and set default value to it
	 *  
	 *  		# Syntax:
	 *  
	 *  			class-name(){
	 *  
	 *  				// set default values to object 
	 *  			}
	 *  
	 *  		b. Parameterized Constructor
	 *  				# used to create object and set dynamic value to it.
	 *  
	 *  		# Syntax:
	 *  
	 *  			class-name(arg1, arg2, .... , argn){
	 *  
	 *  					// set dynamic value to object
	 *  
	 *  			}
	 *  
	 */

}
